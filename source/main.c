int main(void)
{

    SystemInit();

    xTaskCreate(    vTask1,
                    "Task1",
                    128,
                    NULL,
                    1,
                    NULL);

    vTaskStartScheduler();
    while (1)
    {
        return (0);
    }
    
}


void vTask1( void *pvParameters )
{
    for (;;)
    {
        vTaskDelay(10);
    }
    
}
